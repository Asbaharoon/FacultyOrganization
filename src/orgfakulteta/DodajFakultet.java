    private void jtxtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtSearchKeyReleased
        try {
            String sql = "select * from fakulteti where Fakultet=?";
            pst = conn.prepareStatement(sql);
            pst.setString(1, jtxtSearch.getText());
            rs = pst.executeQuery();
            if (rs.next()) {
                String dodaj1 = rs.getString("Fakultet");
                jtxtNaziv.setText(dodaj1);
                String dodaj2 = rs.getString("Dekan");
                jtxtDekan.setText(dodaj2);
                String dodaj3 = rs.getString("Adresa");
                jtxtAdresa.setText(dodaj3);
                String dodaj4 = rs.getString("Telefon");
                jtxtTelefon.setText(dodaj4);
            }
          
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jtxtSearchKeyReleased

