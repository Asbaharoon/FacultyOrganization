    private void popuniCombo() {
        try {
            String sql = "select * from fakulteti";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            while (rs.next()) {
                String fakultet = rs.getString("Fakultet");
                jComboFakulteti.addItem(fakultet);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                rs.close();
                pst.close();
            } catch (Exception e) {
            }
        }
    }

